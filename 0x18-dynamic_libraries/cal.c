#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: Addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: Subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: 
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - Multiply two integers
 * @a: first int
 * @b: second int
 * Return: Multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
